    Color lerpColor(Color begin, Color end, float p) {

        uint8_t r = _lertRgbValue(begin.r, end.r, p);
        uint8_t g = _lertRgbValue(begin.g, end.g, p);
        uint8_t b = _lertRgbValue(begin.b, end.b, p);

        return Color(r, g, b);
    }

    Color getRainbowColor(uint8_t hue) {
        int i = 3 * (int)hue;
        return { _rainbowTable[i], _rainbowTable[i + 1], _rainbowTable[i + 2] };
    }

    int getRandomNumber(int max) {
        return rand() % max;
    }

    static uint8_t _lertRgbValue(float a, float b, float p) {
        float v = a + (b - a) * p;
        if (v < 0) v = 0;
        else if (v > 255) v = 255;

        return v;
    }

